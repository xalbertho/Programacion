#math funtions
import math

pi=3.14;
x,z,y=84,91,23;

print(round(pi)); #redondea de acuerdo a si esta por debajo o encima de .5
print(math.ceil(pi));# redondea al numero por encima
print(math.floor(pi));# redondea al numero por debajo
print(abs(pi));# valor absoluto
print(pow(pi,2)); #elevar a una potencia
print(math.sqrt(pi)); #raiz cuadrada
print(max(x,y,z)); #maximo en una coleccion de datos
print(min(x,y,z)); #minimo en una coleccion de datos

